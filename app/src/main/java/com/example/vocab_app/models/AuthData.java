package com.example.vocab_app.models;

public class AuthData {
    private User user;
    private String token;

    public AuthData() {
    }

    public AuthData(User user, String token) {
        this.user = user;
        this.token = token;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }
}
